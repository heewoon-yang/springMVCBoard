package com.freehoon.web.board.model;

public class MyStudentCommand {
	private int age;
	private String name;
	
	public MyStudentCommand() {}
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
